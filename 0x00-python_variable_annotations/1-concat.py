#!/usr/bin/env python3
"""
Write a type-annotated function `concat` that takes two strings as
arguments and returns the result as a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings together

    Args:
        str1 (str): first string argument
        str2 (str): second string argument
    """
    return str1 + str2
