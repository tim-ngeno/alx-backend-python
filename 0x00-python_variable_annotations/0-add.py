#!/usr/bin/env python3
"""
Write a type annotated function `add` that takes a float `a` and
float `b` as arguments
"""


def add(a: float, b: float) -> float:
    """
    Adds two floating point numbers and returns their sum

    Args:
        a (float): first argument
        b (float): second argument
    """
    return (a + b)
